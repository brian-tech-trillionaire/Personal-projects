# Assigning the string "THE LION AND THE WITCH BOOK" to the variable Marie
Marie = "THE LION AND THE WITCH BOOK"
# Assigning the string "THE HOBBIT BOOK" to the variable Camila
Camila = "THE HOBBIT BOOK"
# Printing the values of Marie and Camila before interchanging their books
print("Before interchanging their books:", "Marie:", Marie, "Camila:", Camila)
# Interchanging the values of Marie and Camila using tuple unpacking
Marie, Camila = Camila, Marie
# Printing the values of Marie and Camila after interchanging their books
print("After interchanging their books:", "Marie:", Marie, "Camila:", Camila)
